
public class Assignmentoperator  
{
	public static void main(String[] args) 
	{
		int x = 4;
		x =x + 5; // x = x + 5
		System.out.println(x);
		
		x= x- 5; // x = x - 5
		System.out.println(x);
		
		x =x* 5; // x = x * 5
		System.out.println(x);
		x =x/ 4; // x = x / 4
		System.out.println(x);
		x =x% 5;   // x = x % 5
		System.out.println(x);
	}
}

